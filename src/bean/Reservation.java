package bean;

public class Reservation {
	private String res_no;
	private String us_id;
	private String sitter_id;
	private String pet_no;
	private String res_date_fr;
	private String res_date_to;
	private String res_price;
	private String res_addr;
	
	
	
	public String getRes_no() {
		return res_no;
	}
	public void setRes_no(String res_no) {
		this.res_no = res_no;
	}
	public String getUs_id() {
		return us_id;
	}
	public void setUs_id(String us_id) {
		this.us_id = us_id;
	}
	public String getSitter_id() {
		return sitter_id;
	}
	public void setSitter_id(String sitter_id) {
		this.sitter_id = sitter_id;
	}
	public String getPet_no() {
		return pet_no;
	}
	public void setPet_no(String pet_no) {
		this.pet_no = pet_no;
	}
	public String getRes_date_fr() {
		return res_date_fr;
	}
	public void setRes_date_fr(String res_date_fr) {
		this.res_date_fr = res_date_fr;
	}
	public String getRes_date_to() {
		return res_date_to;
	}
	public void setRes_date_to(String res_date_to) {
		this.res_date_to = res_date_to;
	}
	public String getRes_price() {
		return res_price;
	}
	public void setRes_price(String res_price) {
		this.res_price = res_price;
	}
	public String getRes_addr() {
		return res_addr;
	}
	public void setRes_addr(String res_addr) {
		this.res_addr = res_addr;
	}
}
