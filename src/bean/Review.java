package bean;

public class Review {
	
	private String res_no;
	private String res_poient;
	private String res_title;
	private String res_cont;
	public String getRes_no() {
		return res_no;
	}
	public void setRes_no(String res_no) {
		this.res_no = res_no;
	}
	public String getRes_poient() {
		return res_poient;
	}
	public void setRes_poient(String res_poient) {
		this.res_poient = res_poient;
	}
	public String getRes_title() {
		return res_title;
	}
	public void setRes_title(String res_title) {
		this.res_title = res_title;
	}
	public String getRes_cont() {
		return res_cont;
	}
	public void setRes_cont(String res_cont) {
		this.res_cont = res_cont;
	}

}
